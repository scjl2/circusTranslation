package hijac.tools.scjmsafe.language.RefCon;

public class Current extends MetaRefCon {

    public Current() { }

    public void printMetaRefCon() {
        System.out.print("Current");
    }

    public String getMetaRefConString() {
        return "Current";
    }

    public boolean equalTo(MetaRefCon arg) {
        if (arg instanceof Current) {
            return true;
        } else {
            return false;
        }
    }

    public Current clone() {
        return new Current();
    }

}
