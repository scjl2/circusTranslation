package accs;

import javax.realtime.*;

import javax.safetycritical.*;

public class Events {
   /* Events */
   public final AperiodicEvent wheelshaft;
   public final AperiodicLongEvent engine;
   public final AperiodicLongEvent brake_engaged;
   public final AperiodicLongEvent brake_disengaged;
   public final AperiodicLongEvent top_gear_engaged;
   public final AperiodicLongEvent top_gear_disengaged;
   public final AperiodicEvent lever;

   /* Create Events */
   public Events() {
      wheelshaft = new AperiodicEvent();
      engine = new AperiodicLongEvent();
      brake_engaged = new AperiodicLongEvent();
      brake_disengaged = new AperiodicLongEvent();
      top_gear_engaged = new AperiodicLongEvent();
      top_gear_disengaged = new AperiodicLongEvent();
      lever = new AperiodicEvent();
   }
}
